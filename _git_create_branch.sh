#!/bin/bash

#=============================================================================
# Gitブランチ作成スクリプト
#=============================================================================
# 【概要】
#   GitHub Issueの番号を指定して、feature/issue-<番号> の形式でブランチを作成します。
#
# 【使用方法】
#   ./_git_create_branch.sh <issue番号>
#
# 【使用例】
#   ./_git_create_branch.sh 123
#   → feature/issue-123 ブランチが作成されます
#
# 【前提条件】
#   - Gitリポジトリ内で実行すること
#   - 作成前に現在のブランチの変更をコミットまたはstashしておくこと
#
# 【注意事項】
#   - 同名のブランチが既に存在する場合はエラーになります
#   - ブランチ作成後、自動的に新しいブランチに切り替わります
#=============================================================================

# 引数チェック：issue番号が指定されているか確認
if [ -z "$1" ]; then
  echo "エラー: issue番号を指定してください"
  echo "使用方法: $0 <issue番号>"
  echo "例: $0 123"
  exit 1
fi

# feature/issue-<番号> の形式でブランチを作成し、切り替え
echo "ブランチ feature/issue-$1 を作成しています..."
git checkout -b feature/issue-"$1"

# 結果を確認
if [ $? -eq 0 ]; then
  echo "✓ ブランチ feature/issue-$1 が正常に作成されました"
else
  echo "✗ ブランチの作成に失敗しました"
  exit 1
fi
